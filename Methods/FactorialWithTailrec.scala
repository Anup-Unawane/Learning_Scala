/***
	Recursive methods may run out of stack.
	Convert recursive methods into tail recursive methods.
	Tail recursive methods after evaluation must be returned.
**/

import scala.annotation.tailrec

@tailrec
def fact(n:BigInt, acc:BigInt):BigInt =
{
	if(n == 1 || n == 1 ) acc
	else fact(n - 1, acc * n)
}

def factorial(n:BigInt) = fact(n , 1)

